package step1_input_output;

import java.util.Scanner;

public class Problem_10430 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int A = sc.nextInt();
		int B = sc.nextInt();
		int C = sc.nextInt();
		
		System.out.println((A+B)%C);
		System.out.println((A+B)%C);
		System.out.println((A*B)%C);
		System.out.println((A*B)%C);
	}

}
